#include "justlib.h"

int	ft_count_chr(const char *s, const char *chrs)
{
	int			i;
	int			j;

	i = 0;
	j = 0;
	while (s[i])
		if (ft_is_separator(s[i++], chrs) == 0)
			j++;
	return (j);
}
