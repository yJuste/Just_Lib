#include "justlib.h"

int	ft_next_chr(const char *s, char chr)
{
	static int		i = 0;

	if (!chr)
	{
		i = 0;
		return (-1);
	}
	while (s[i])
	{
		if (s[i] == chr)
			return (i++);
		i++;
	}
	return (-1);
}
