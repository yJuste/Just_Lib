/*   • Duplicates a str.                                                      */
/*   • Prototype:   VOID ( char *, char * )                                   */
/*        -> size_t                                                           */
/* ************************************************************************** */
#include "just_lib_stack.h"

void	ft_strdup_stack(char *src, char *dest)
{
	size_t		i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
}
